package javachr.rules.head;


/**
 * This enum stores the different types {@link Head}-objects can have.
 * ANY: The {@link Head}-object is not restricted to a specific type or value.
 * TYPE: The {@link Head}-object is restricted to a specific type/class.
 * VALUE: The {@link Head}-object is restricted to a specific value.
 */
public enum HEAD_CONTAINS {
    ANY, TYPE, VALUE
}